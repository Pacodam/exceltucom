package model;

public class SheetA {

	private String name;
	private String href;
	
	public SheetA(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public String getHref() {
		return href;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setHref(String href) {
		this.href = href;
	}
	
	
	
	
}
